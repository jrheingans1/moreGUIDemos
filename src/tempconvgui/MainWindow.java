    private void btnConvertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConvertActionPerformed
        // TODO add your handling code here:
        String strTemp = this.txtTemp.getText();
        double origTemp = 0.0;
        double converted = 0.0;
        
        if(this.rdoFahrenheitToCelcius.isSelected()) {
            origTemp = getTemperatureInput(strTemp, origTemp);
            converted = (origTemp - 32) * 5/9;
            displayConversion(converted);
        } else {
            origTemp = getTemperatureInput(strTemp, origTemp);
            converted = origTemp * 9/5 + 32;
            displayConversion(converted);
        }
        
        // now perform an operation that takes a lot of time
        // by running it in a separate thread
        new Thread(new Runnable() {
            public void run() {
                for(long l=0; l < 1000000000000L; l++) {
                    System.out.println("Long running iteration #" + l);
                }
            }
        }).start();

    }//GEN-LAST:event_btnConvertActionPerformed

    
    
    
