    private double getTemperatureInput(String strTemp, double origTemp) throws HeadlessException {
        try {
            origTemp = Double.parseDouble(strTemp);
        } catch(NumberFormatException nfe) {
            displayErrorMsg();
        }
        return origTemp;
    }

